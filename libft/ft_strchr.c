#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*temp;

	temp = (char *)s;
	while (*temp != (char)c)
	{
		if (*temp == '\0')
			return (0);
		temp++;
	}
	return (temp);
}
